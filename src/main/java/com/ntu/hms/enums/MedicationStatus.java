package com.ntu.hms.enums;

/**
 * Enum representing the status of medication stock levels in an inventory.
 *
 * <p>The statuses indicate the level of the stock as follows: - LOW: The stock is low and may need
 * to be replenished. - MEDIUM: The stock level is moderate. - HIGH: The stock level is high.
 */
public enum MedicationStatus {
  LOW,
  MEDIUM,
  HIGH
}
