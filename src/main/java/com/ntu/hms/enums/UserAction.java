package com.ntu.hms.enums;

/**
 * Enum representing the possible actions a user can take in an application.
 *
 * <p>This enum contains the following actions: - LOGIN: Represents the action where a user logs
 * into the system. - EXIT: Represents the action where a user exits or logs out of the system.
 */
public enum UserAction {
  LOGIN,
  EXIT
}
